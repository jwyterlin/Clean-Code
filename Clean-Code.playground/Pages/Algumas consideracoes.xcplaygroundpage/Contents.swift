 O princípio dos 5S’s:

 - Seiri ( Organização ), ordenar, saber onde estão as coisas, nomes adequados
 - Seiton ( arrumação ), um pedaço de código deve estar onde se espera
 - Seiso ( limpeza ), refatoração frequente
 - Seiketsu ( padronização ), a equipe concorda em manter o código limpo, padronizado e com práticas consistentes
 - Shutsuke ( disciplina ), disciplina para seguir as práticas e refletir frequentemente isso no trabalho

 Esperar que bugs apareçam deve ser a exceção

 Inspecionamos as máquinas todos os dias e consertamos as partes desgastadas antes de quebrarem

 Tornar seu código legível é tão importante quanto torná-lo executável


 Acoplamento e Coesão

 Acoplamento - união ou ligação entre dois ou mais corpos, formando um único conjunto.
 
 Coesão - União harmônica entre uma coisa e outra; Coerência de pensamento; fundamento que dá sentido a uma obra
